/**
 * Get workflow for task category
 */
export function getWorkflowForCategory(category: string): WorkflowStep[] {
  switch (category) {
    case "bug":
      return bugFixWorkflow;
    case "documentation":
      return documentationWorkflow;
    case "feature":
    default:
      return featureDevelopmentWorkflow;
  }
}
